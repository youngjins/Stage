package kr.co.vo;

public class MenuVO {
	private int MENU_CODE;
	private String UPPER_MENU_CODE;
	private String MENU_NAME;
	private String MENU_EXPLAIN;
	private String USE_YN;
	
	
	public int getMENU_CODE() {
		return MENU_CODE;
	}
	public void setMENU_CODE(int mENU_CODE) {
		MENU_CODE = mENU_CODE;
	}
	public String getUPPER_MENU_CODE() {
		return UPPER_MENU_CODE;
	}
	public void setUPPER_MENU_CODE(String uPPER_MENU_CODE) {
		UPPER_MENU_CODE = uPPER_MENU_CODE;
	}
	public String getMENU_NAME() {
		return MENU_NAME;
	}
	public void setMENU_NAME(String mENU_NAME) {
		MENU_NAME = mENU_NAME;
	}
	public String getMENU_EXPLAIN() {
		return MENU_EXPLAIN;
	}
	public void setMENU_EXPLAIN(String mENU_EXPLAIN) {
		MENU_EXPLAIN = mENU_EXPLAIN;
	}
	public String getUSE_YN() {
		return USE_YN;
	}
	public void setUSE_YN(String uSE_YN) {
		USE_YN = uSE_YN;
	}
	
	
}
